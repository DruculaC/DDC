/*---------------------------------------------------
	Delay()
	延迟程序
--------------------------------------------------*/

void Delay(unsigned int n)
{
	unsigned int x;
	while(n--)
	{
		x=5000;
		while(x--);
	}
}

/*---------------------------------------------------
	Delay3()
	延迟程序
--------------------------------------------------*/

void Delay3(unsigned int t)
{
	unsigned int i,j;
	for(i=0;i<t;i++)		
	for(j=0;j<19;j++);
}

/*---------------------------------------------------
	Delay4()
	延迟程序
--------------------------------------------------*/

void Delay4(unsigned int t)
{
	unsigned int i,j;
	for(i=0;i<t;i++)		
	for(j=0;j<26;j++);
}
	
/*--------------------------------------------------
	delay_ms()
	
	延迟约为1ms的子程序
----------------------------------------------------*/

void delay_ms(unsigned int count)
{
	unsigned int i;
	unsigned int j;
	for(i=0;i<count;i++)
	{
		for(j=0;j<500;j++);
	}
}

/*--------------------------------------------------
	delay_us()
	
	延迟约为1us的子程序
----------------------------------------------------*/

void delay_us(unsigned int count1)
{
	unsigned int i;
	unsigned char j;
	for(i=0;i<count1;i++)
	{
		for(j=0;j<20;j++);
	}
}

